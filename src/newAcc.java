public class newAcc{
        String prefix;
        String firstName;
        String lastName;
        String accountType;        
        String gender;
        String month;
        int date;
        int year;
        String contact;
        String email;
        String occupation;
        String ad1;
        String ad2;
        String city;
        String state;
        String postalCode;
        String maritial;
        String aadharCard;
        String accNo;
        
    newAcc(String prefix,
        String firstName,
        String lastName,
        String accountType,        
        String gender,
        String month,
        int date,
        int year,
        String contact,
        String email,
        String occupation,
        String ad1,
        String ad2,
        String city,
        String state,
        String postalCode,
        String maritial,
        String aadharCard,
        String accNo){
        
        this.prefix = prefix;
        this.firstName = firstName;
        this.lastName = lastName;
        this.accountType = accountType;
        this.gender = gender;
        this.month = month;
        this.date = date;
        this.year = year;
        this.contact = contact;
        this.email = email;
        this.occupation = occupation;
        this.ad1 = ad1;
        this.ad2 = ad2;
        this.city = city;
        this.state = state;
        this.postalCode = postalCode;
        this.maritial = maritial;
        this.aadharCard = aadharCard;
        this.accNo = accNo;
    }
}
